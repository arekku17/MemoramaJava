package controlador;

public class Nivel {
    int num;
    String nivel;

    public Nivel(int num, String nivel) {
        this.num = num;
        this.nivel = nivel;
    }

    
    
    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getNivel() {
        return nivel;
    }

    public void setNivel(String nivel) {
        this.nivel = nivel;
    }
    
}
